# Arithmetic Operators:
a = 5
b = 2
print (a+b) #7
print (a-b) #3
print (a*b) #10

#division
print(a/b) #2.5
print(a//b) #2 (Quotient)
print(a%b) #1 (Remainder)

#Exponential
print (a**b) #25 (5**2)

